    module_id: str
    name: str
    layer: int
    layer_name: str
    toggleable: bool               # False for Layer 1 core modules
    applicable_hts_chapters: list[str]  # 2-digit strings; empty = all chapters
    applicable_keywords: list[str]      # terms to scan for in document text
    cert_number_patterns: list[str]     # regex patterns for certificate numbers
    risk_countries: list[str]           # ISO2 codes that elevate risk for this module
    description: str
    why_it_matters: str


